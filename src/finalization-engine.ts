async function scheduleEmergencyFinalization(projectAddress: string, env: Env) {
  // ‚≠ê ACƒ∞L DURUM ƒ∞√áƒ∞N Y√ñNETƒ∞Cƒ∞ ONAYI BEKLE
  await env.db(
    'UPDATE projects SET finalization_status = $1, emergency_lock_reason = $2, emergency_lock_timestamp = NOW() WHERE contract_address = $3',
    ['NEEDS_EMERGENCY_APPROVAL', 'Key management issue during finalization', projectAddress.toLowerCase()]
  );
  
  console.log(`[EMERGENCY-REQUIRED] Project ${projectAddress} needs admin approval for finalization`);
  
  // ‚≠ê ADMƒ∞NLERE OTOMATƒ∞K Bƒ∞LDƒ∞Rƒ∞M G√ñNDER
  await sendEmergencyNotification(projectAddress, env);
}

async function sendEmergencyNotification(projectAddress: string, env: Env) {
  // Discord/Telegram/SMS bildirimi
  const adminWebhook = env.ADMIN_EMERGENCY_WEBHOOK;
  if (adminWebhook) {
    await fetch(adminWebhook, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        content: `üö® EMERGENCY: Project ${projectAddress} needs manual finalization approval.

Reason: Key management issue

Dashboard: https://admin.lolhub.fun/projects/${projectAddress}`
      })
    });
  }
}